// Функция для добавления отзывов напрямую в localStorage
export function addReviewsDirectly() {
  // Полностью заменяем все отзывы на новые креативные отзывы
  localStorage.setItem('fitgenius_reviews_published', JSON.stringify(directReviews));
  
  // Очищаем также ожидающие отзывы, если они есть
  localStorage.setItem('fitgenius_reviews_pending', JSON.stringify([]));
  
  console.log(`Заменено все отзывы на ${directReviews.length} креативных отзывов`);
  return directReviews.length;
}

// Функция для проверки наличия отзывов
export function checkReviews() {
  const reviews = JSON.parse(localStorage.getItem('fitgenius_reviews_published') || '[]');
  console.log(`В localStorage сейчас ${reviews.length} отзывов:`, reviews);
  return reviews.length;
}
